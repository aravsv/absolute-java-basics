package com.javatut.section3;

public class IfElse {

	public static void main(String[] args) {

		int number = 50;
		
		if (number == 20) {
			System.out.println("number is equal to 20");
		} else if (number > 20) { 
			System.out.println("number is greater than 20");
		} else {
			System.out.println("number is not equal to 20");
		}
		
	}

}
